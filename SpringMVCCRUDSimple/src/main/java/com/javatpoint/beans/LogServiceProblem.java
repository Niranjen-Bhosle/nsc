package com.javatpoint.beans;

public class LogServiceProblem {
	private String name;
	private String organization;
	private String designation;
	private String logserviceproblem;
	private  String description;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOrganization() {
		return organization;
	}
	public void setOrganization(String organization) {
		this.organization = organization;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getLogserviceproblem() {
		return logserviceproblem;
	}
	public void setLogserviceproblem(String logserviceproblem) {
		this.logserviceproblem = logserviceproblem;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
}
